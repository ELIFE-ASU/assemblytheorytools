def origami(mol):
    """
    Execute intramolecular cyclization reactions on a molecule using origami SMARTS patterns.
    
    This function performs "origami" transformations - intramolecular reactions where a single
    molecule undergoes internal bond formation to create cyclic structures. The method applies
    all available origami reaction patterns and returns valid cyclized products that meet
    strict validation criteria.

    Algorithm:
        1. Retrieves origami SMARTS patterns from origami_smarts()
        2. Converts SMARTS patterns to RDKit reaction objects
        3. For each reaction pattern:
           - Applies reaction to input molecule
           - Validates products using strict criteria
           - Collects unique valid products
        4. Returns list of cyclized molecules or original molecule if no products
    
    Args:
        mol (rdkit.Chem.Mol): Input molecule to undergo intramolecular cyclization.
                             Should contain reactive sites capable of internal bond formation.
    
    Returns:
        list[rdkit.Chem.Mol]: List of cyclized product molecules:
            - If successful: List of unique valid cyclization products
            - If no products: List containing only the original input molecule

    """

    # Get the allowed operations
    smarts = origami_smarts()

    rxn = [AllChem.ReactionFromSmarts(s) for s in smarts]

    all_products = []
    unique_products = set()
    num_atoms = mol.GetNumAtoms()

    for reaction in rxn:
        try:
            for product in reaction.RunReactant(mol, 0):
                if product[0].GetNumAtoms() == num_atoms - 1:
                    unique_products.add(Chem.MolToSmiles(product[0]))
        except:
            continue

    all_products.extend(unique_products)

    if not all_products:
        print("No origami products found...", flush=True)
        return [mol]
    else:
        # Convert the unique SMILES strings back to RDKit molecule objects
        new_mol_list = [Chem.MolFromSmiles(smiles) for smiles in all_products]
        return new_mol_list


def conformation_filter(mol):
    """
    Generate a 3D conformation for a molecule and filter based on geometry generation success.

    This function processes an input molecule through several steps: sanitization, hydrogen
    addition, and 3D conformation generation using RDKit's ETKDGv3 (Experimental Torsion
    Knowledge Distance Geometry version 3) method. The function serves as both a conformer
    generator and a molecular filter, returning only molecules for which valid 3D geometries
    can be successfully generated.

    Algorithm:
        1. Sanitize the input molecule (standardize bond orders, aromaticity, etc.)
        2. Add explicit hydrogen atoms to all heavy atoms
        3. Configure ETKDGv3 parameters with fixed random seed for reproducibility
        4. Attempt 3D coordinate embedding using distance geometry
        5. Return molecule if successful (score=0) or None if failed (score=-1 or other)

    Parameters
    ----------
    mol : rdkit.Chem.Mol
        The input molecule object to process. Should be a valid
        RDKit molecule, though basic sanitization will be attempted.

    Returns
    -------
    rdkit.Chem.Mol or None
        Molecule with generated 3D conformation and explicit hydrogens if successful,
        None if geometry generation fails or encounters errors.
    """

    Chem.SanitizeMol(mol)
    mol = Chem.AddHs(mol)
    params = AllChem.ETKDGv3()
    params.randomSeed = 0xf00d  # optional random seed for reproducibility
    score = AllChem.EmbedMolecule(mol, params)

    if score == 0:
        return mol
    elif score == -1:
        print("Geometry generation failed", flush=True)
        return None
    else:
        return None


def pick_two(to_be_combined):
    """
    Randomly select two unique elements from a list and return them with remaining elements.
    
    This function performs random sampling without replacement to select exactly two distinct
    elements from the input list. It's designed for scenarios where you need to randomly
    pair elements while maintaining separation between selected and unselected items.

    Algorithm:
        1. Validates input list length (returns empty lists if <= 1 elements)
        2. Uses numpy.random.choice() to select 2 unique indices without replacement
        3. Extracts elements at selected indices to create 'picked' list
        4. Creates 'remain' list from all non-selected indices (preserves original order)
        5. Returns both lists as tuple
    
    Args:
        to_be_combined (list): Input list of elements to sample from. Can contain any type
                              of objects (molecules, strings, numbers, etc.). Must have
                              length >= 2 for meaningful operation.
    
    Returns:
        tuple[list, list]: A tuple containing two lists:
            - picked (list): List of exactly 2 randomly selected unique elements
            - remain (list): List of all remaining elements (original order preserved)
            
        Special cases:
            - If input length <= 1: Returns ([], []) - two empty lists
            - If input length >= 2: Returns normal (picked, remain) tuple
    """
    if len(to_be_combined) <= 1:
        return [], []

    temp = np.random.choice(len(to_be_combined), 2, replace=False)
    picked = [to_be_combined[i] for i in temp]
    remain = [to_be_combined[i] for i in range(len(to_be_combined)) if i not in temp]

    return picked, remain


def get_num_atom(formula, elements):
    """
    Extract the number of atoms for a specific element from a molecular formula string.
    
    This function parses a molecular formula to determine how many atoms of a specified
    element are present. It handles standard chemical notation where element symbols
    are followed by optional numeric subscripts indicating atom counts.

    Algorithm:
        1. Searches for first occurrence of element symbol in formula
        2. If not found, returns 0
        3. Extracts substring after element symbol
        4. Parses numeric characters following element until next alphabetic character
        5. Returns parsed number, or 1 if no number follows element
    
    Args:
        formula (str): Molecular formula string in standard chemical notation.
                      Examples: 'H2O', 'C6H12O6', 'CaCl2', 'CH3OH'
        elements (str): Element symbol to search for (case-sensitive).
                       Should match standard periodic table symbols: 'H', 'C', 'O', 'Ca', etc.
    
    Returns:
        int: Number of atoms of the specified element in the formula:
            - 0 if element is not found in the formula
            - 1 if element is present without explicit count (e.g., 'O' in 'H2O')
            - n if element has explicit count (e.g., 2 for 'H' in 'H2O')
    """
    idx1 = formula.find(elements)
    if idx1 == -1:
        return 0
    temp = formula[idx1 + len(elements):]
    if not temp:
        return 1
    try:
        idx2 = next(i for i, c in enumerate(temp) if c.isalpha())
    except StopIteration:
        return int(temp)
    n_str = temp[:idx2]
    return int(n_str) if n_str else 1


def degree_unsaturation(mol):
    """
    Calculate the degree of unsaturation (DoU) for a given molecule.

    The degree of unsaturation is a measure of the number of rings and multiple bonds in a molecule.
    It is calculated using the molecular formula of the molecule.

    Args:
        mol (rdkit.Chem.Mol): The molecule for which the degree of unsaturation is to be calculated.

    Returns:
        float: The degree of unsaturation of the molecule.
    """
    formula = rdMolDescriptors.CalcMolFormula(mol)
    n_carbon = get_num_atom(formula, 'C')
    n_nitrogen = get_num_atom(formula, 'N')
    n_halogen = sum(get_num_atom(formula, halogen) for halogen in ['F', 'Cl', 'Br', 'I'])
    n_hydrogen = get_num_atom(formula, 'H')
    dou = (2 * n_carbon + 2 + n_nitrogen - n_halogen - n_hydrogen) / 2
    return dou


def get_unique_mols(mols):
    """
    Takes a list of RDKit molecule objects and returns a list of unique molecules.
    Uniqueness is determined by comparing InChI representations.

    Args:
        mols (list): List of RDKit molecule objects

    Returns:
        list: List of unique RDKit molecule objects
    """
    if not mols:
        return []

    unique_mols = []
    seen_inchis = set()

    for mol in mols:
        if mol is None:
            continue

        # Generate InChI for the molecule
        try:
            inchi = Chem.MolToInchi(mol)

            # If this InChI hasn't been seen before, add the molecule to the result
            if inchi not in seen_inchis:
                seen_inchis.add(inchi)
                unique_mols.append(mol)
        except:
            # Skip molecules that can't be converted to InChI
            continue

    return unique_mols


def reassemble_old(mols,
                   n_mol_needed=1000,
                   mw_min=20,
                   mw_max=100,
                   unsat_min=1.0,
                   unsat_max=12.0,
                   one_atom_weight=2,
                   mw_delta=0.1
                   ):
    """
    Generate new molecules by reassembling molecular fragments with comprehensive filtering.
    
    This function creates novel molecular structures by systematically combining input molecular
    fragments, applying origami cyclization operations, and filtering results based on multiple
    physicochemical criteria.

    Algorithm:
        1. **Fragment Pool Preparation**: Deduplicate input fragments and display pool info
        2. **Fragment Selection**: Randomly select fragments within MW constraints
        3. **Assembly Process**: Iteratively combine fragments using assembly reactions
        4. **Unsaturation Adjustment**: Apply origami cyclization if DoU is too low
        5. **Multi-Stage Filtering**: Apply structural and conformational filters
        6. **Output Generation**: Collect valid molecules and create visualizations

    Parameters
    ----------
    mols : list of rdkit.Chem.Mol
        Input molecular fragments to be reassembled.
        Will be deduplicated automatically.
    n_mol_needed : int, optional
        Target number of new molecules to generate, by default 1000.
    mw_min : float, optional
        Minimum molecular weight for generated molecules, by default 20.
    mw_max : float, optional
        Maximum molecular weight for generated molecules, by default 100.
    unsat_min : float, optional
        Minimum degree of unsaturation (double bond equivalents), by default 1.0.
    unsat_max : float, optional
        Maximum degree of unsaturation (double bond equivalents), by default 12.0.
    one_atom_weight : float, optional
        Weight adjustment factor for atom overlap during fragment assembly, by default 2.
    mw_delta : float, optional
        Molecular weight tolerance factor for fragment selection.
        Creates MW range: [mw_min*(1-delta), mw_max*(1+delta)], by default 0.1.
    
    Returns
    -------
    list of rdkit.Chem.Mol
        List of successfully generated and validated molecules.
        Length may be less than n_mol_needed if generation fails.
    """

    # Ensure that the input list of molecules is unique
    mols = get_unique_mols(mols)

    n_frag = len(mols)
    print('Number of virtual objects in the assembly pool:', n_frag, flush=True)
    print('Virtual objects in the assembly pool:', flush=True)
    for i in range(n_frag):
        print(Chem.MolToInchi(mols[i]), flush=True)

    mw_min_try = mw_min * (1 - mw_delta)
    mw_max_try = mw_max * (1 + mw_delta)
    n = 0
    new_mols = []
    n_filtered = 0

    while n < n_mol_needed:
        print(f'Searching for new molecules... {n}', flush=True)

        mw = one_atom_weight
        idx_list = []
        idx = []
        while True:
            # to obtain idxList that contains a list of (list of fragments)
            # each (list of fragments) is within the molecular weight range [mw_min_try, mw_max_try]
            # so later, we randomly choose one (list of fragments) is a proper set of fragments that when they're combined, the weight is within the required range.
            idx_this = random.randint(0, n_frag - 1)
            mw += rdMolDescriptors.CalcExactMolWt(mols[idx_this], onlyHeavy=True)
            if mw < mw_min_try:
                idx.append(idx_this)
                mw -= one_atom_weight
            else:
                if mw <= mw_max_try:
                    idx.append(idx_this)
                    idx_list.append(idx.copy())
                    mw -= one_atom_weight
                else:
                    break

        continue_search = False
        if len(idx_list) == 0:
            print('Warning: mwMin and mwMax may be too close.', flush=True)
        else:
            to_be_comb = idx_list[random.randint(0, len(idx_list) - 1)]
            print(f'To be combined: {to_be_comb}', flush=True)

            i_frag = n_frag
            while len(to_be_comb) > 1:
                # combine fragments until only a big one exists.
                # randomly pick two fragments from to_be_comb and combine them, and then put it back into to_be_comb; Then randomly pick two from to_be_comb and repeat.
                picked, remain = pick_two(to_be_comb)
                m1 = mols[picked[0]]
                m2 = mols[picked[1]]

                if m1.GetNumBonds() == 1 or m2.GetNumBonds() == 1:
                    # when either has only one bond, one atom can be joined ("overlapped")
                    new_mol = assemble(m1, m2, 1)
                else:
                    # otherwise, decide whether overlap 1 or 2 atoms
                    new_mol = assemble(m1, m2, random.randint(1, 2))

                if new_mol is None:
                    # it could happen that combination fails
                    continue_search = True
                    break
                else:
                    if i_frag >= len(mols):
                        mols.append(new_mol)
                    else:
                        mols[i_frag] = new_mol
                    remain.append(i_frag)
                    i_frag += 1
                    to_be_comb = remain
            if continue_search:
                continue
            new_molecule = mols[to_be_comb[0]]
            print(f'New molecule: {Chem.MolToSmiles(new_molecule)}', flush=True)

            # consider Degree of Unsaturation
            dou = degree_unsaturation(new_molecule)
            print(f'Degree of unsaturation: {dou}', flush=True)
            if dou > unsat_max:
                print('dou > unsat_max', flush=True)
                continue
            elif dou < unsat_min:
                print('dou < unsat_min', flush=True)
                # if DoU is smaller than DoUMin, then use operation Origami() to make rings (details in Reassembler.py)
                # one Origami() operation, DoU is increased by 1.
                n_origami = random.randint(int(unsat_min - dou), int(unsat_max - dou))
                print(f'Origami operations: {n_origami}', flush=True)
                # Randomly decide how many Origami() operation will be done
                new_molecule = [new_molecule]
                for i in range(n_origami):
                    print(f'Origami operation {i + 1}', flush=True)
                    in_molecule = random.choice(new_molecule)
                    out_molecule = origami(in_molecule)
                    if len(out_molecule) == 1 and out_molecule[0] == in_molecule:
                        # exit the loop if no new molecule is generated
                        break

                    # otherwise, continue to do origami operation
                    new_molecule = out_molecule
            else:
                pass

            # apply filter_mol which is commonly used to filter obviously impossible molecules
            new_molecule = filter_mol(new_molecule)
            if new_molecule is None:
                print(':', end='', flush=True)
                n_filtered += 1
                continue

            # apply conformation_filter which filters molecules that do not have valid conformations
            new_molecule = conformation_filter(new_molecule)
            if new_molecule is None:
                print(':', end='', flush=True)
                n_filtered += 1
                continue

            # then, a new molecule is found
            n += 1
            print(n, end=', ', flush=True)
            new_mols.append(new_molecule)

    print('\n', 'nFiltered =', n_filtered, flush=True)

    # Visualize, generate figures
    # printer(new_mols)

    return new_mols


def enumerate_sterioisomers(mol):
    """
    Enumerate all possible stereoisomers of a given molecule.

    This function takes an RDKit molecule object as input and generates all possible stereoisomers
    of the molecule. It returns a list of RDKit molecule objects representing these stereoisomers.

    Parameters
    ----------
    mol : rdkit.Chem.Mol
        The input molecule for which stereoisomers are to be enumerated.

    Returns
    -------
    list of rdkit.Chem.Mol
        A list of RDKit molecule objects representing the stereoisomers of the input molecule.
    """
    # Define options for stereoisomer enumeration
    opts = StereoEnumerationOptions(unique=True, onlyUnassigned=False)

    # Enumerate stereoisomers based on the defined options
    isomers = EnumerateStereoisomers(mol, options=opts)

    # Convert the enumerated stereoisomers to SMILES strings and sort them
    mol_out = [smi for smi in sorted(Chem.MolToSmiles(x, isomericSmiles=True) for x in tuple(isomers))]

    # Convert the sorted SMILES strings back to RDKit molecule objects and return them
    return [Chem.MolFromSmiles(x) for x in mol_out]


def enumerate_tautomers(mol):
    """
    Enumerate all possible tautomers of a given molecule.

    This function takes an RDKit molecule object as input and generates all possible tautomers
    of the molecule. It returns a list of RDKit molecule objects representing these tautomers.

    Parameters
    ----------
    mol : rdkit.Chem.Mol
        The input molecule for which tautomers are to be enumerated.

    Returns
    -------
    list of rdkit.Chem.Mol
        A list of RDKit molecule objects representing the tautomers of the input molecule.
    """
    # Create a TautomerEnumerator object
    enumerator = rdMolStandardize.TautomerEnumerator()

    # Canonicalise the input molecule to get the canonical tautomer
    canon = enumerator.Canonicalize(mol)

    # Convert the canonical tautomer to a SMILES string
    csmi = Chem.MolToSmiles(canon)

    # Enumerate all possible tautomers of the input molecule
    tauts = enumerator.Enumerate(mol)

    # Create a list of the canonical tautomer and the sorted tautomers (excluding the canonical tautomer)
    res = [canon] + [y for x, y in sorted((Chem.MolToSmiles(x), x) for x in tauts) if x != csmi]

    return res


def enumerate_heterocycles(mol, depth=None):
    """
    Enumerate all possible heterocycles of a given molecule.

    This function takes an RDKit molecule object as input and generates all possible heterocycles
    of the molecule. It returns a list of RDKit molecule objects representing these heterocycles.

    Parameters
    ----------
    mol : rdkit.Chem.Mol
        The input molecule for which heterocycles are to be enumerated.
    depth : int, optional
        The depth of enumeration. Defaults to None.

    Returns
    -------
    list of rdkit.Chem.Mol
        A list of RDKit molecule objects representing the heterocycles of the input molecule.
    """
    smiles = sorted(Chem.MolToSmiles(m) for m in EnumerateHeterocycles(mol, depth=depth))
    return [Chem.MolFromSmiles(smi) for smi in smiles]


def react_smiles(smiles1, smiles2, random_bond=False, con_filter=True):
    """
    React two molecules by random bond formation - a chemically naive alternative to SMARTS-based assembly.

    This function provides a simple, brute-force approach to molecular combination by randomly
    connecting heavy atoms from two input molecules. Unlike reassembly methods that use chemical reaction patterns, 
    this function ignores chemical realism in favor of speed and simplicity. 
    May create impossible/unstable structures. Ignores valence and hybridization.

    Parameters
    ----------
    smiles1 : str
        SMILES string of the first molecule to combine.
    smiles2 : str
        SMILES string of the second molecule to combine.
    random_bond : bool, optional
        If True, randomly selects bond order (SINGLE/DOUBLE/TRIPLE).
        If False, uses single bonds only, by default False.
    con_filter : bool, optional
        If True, applies 3D conformation filter for validation.
        If False, only basic sanitization, by default True.

    Returns
    -------
    str or None
        SMILES string of combined molecule if successful, None if combination fails.
    """
    try:
        # Convert SMILES strings to RDKit molecule objects
        mol1 = Chem.MolFromSmiles(smiles1)
        mol2 = Chem.MolFromSmiles(smiles2)

        # Define SMARTS pattern to match heavy atoms (non-hydrogen atoms)
        heavy_atom_smarts = Chem.MolFromSmarts("[!#1]")
        heavy_atoms1 = mol1.GetSubstructMatches(heavy_atom_smarts)
        heavy_atoms2 = mol2.GetSubstructMatches(heavy_atom_smarts)

        # Randomly select one heavy atom from each molecule
        idx1 = random.choice(heavy_atoms1)[0]
        idx2 = random.choice(heavy_atoms2)[0]

        # Combine the two molecules into one editable molecule
        emol = Chem.EditableMol(Chem.CombineMols(mol1, mol2))

        # Randomise bond order (SINGLE, DOUBLE, or TRIPLE) if specified
        if random_bond:
            bond_types = [
                Chem.rdchem.BondType.SINGLE,
                Chem.rdchem.BondType.DOUBLE,
                Chem.rdchem.BondType.TRIPLE
            ]
            random_bond = random.choice(bond_types)
            emol.AddBond(idx1, mol1.GetNumAtoms() + idx2, order=random_bond)
        else:
            # Use a fixed bond order (SINGLE)
            emol.AddBond(idx1, mol1.GetNumAtoms() + idx2, order=Chem.rdchem.BondType.SINGLE)

        # Finalise the molecule
        new_mol = emol.GetMol()

        # Apply the conformation filter if specified
        if con_filter:
            new_mol = conformation_filter(new_mol)
        else:
            # Sanitise the new molecule
            Chem.SanitizeMol(new_mol)

        # Return the SMILES string of the new molecule
        return Chem.MolToSmiles(new_mol)
    except:
        # Return None if an error occurs
        return None


def reassemble(mol_pool,
               n_mol_needed=20,
               max_run=10000,
               random_bond=False,
               con_filter=True,
               recycle_to_pool=True,
               max_heavy_atoms=20,
               sterioisomers=False,
               tautomers=False,
               heterocycles=False):
    """
    Simple iterative molecular reassembly using random bond formation - a streamlined alternative to reassemble_old.
    
    This function provides a simplified approach to molecular generation by iteratively combining
    molecules from a pool using random bond formation. Unlike reassemble_old's sophisticated
    fragment-based assembly with SMARTS reactions, this method uses the chemically naive
    react_smiles() function for rapid exploration of molecular space with optional structural
    diversification through stereoisomers, tautomers, and heterocycles.

    Algorithm:
        1. **Random Selection**: Pick two molecules randomly from current pool
        2. **Size Check**: Skip if combined molecule would exceed max_heavy_atoms
        3. **Combination**: Use react_smiles() for random bond formation
        4. **Diversification**: Optionally enumerate stereoisomers/tautomers/heterocycles
        5. **Collection**: Add valid products to output set
        6. **Recycling**: Optionally add new molecules back to pool for further reactions
        7. **Termination**: Stop when n_mol_needed reached or max_run iterations exceeded
    
    Args:
        mol_pool (set): Initial pool of SMILES strings to combine. Pool grows during execution
                       if recycle_to_pool=True.
        n_mol_needed (int, optional): Target number of unique molecules to generate.
                                     Default: 20 (much smaller than reassemble_old's 1000)
        max_run (int, optional): Maximum iterations before termination. Default: 10,000
        random_bond (bool, optional): If True, uses random bond orders (SINGLE/DOUBLE/TRIPLE).
                                     If False, uses single bonds only. Default: False
        con_filter (bool, optional): Apply 3D conformation validation filter. Default: True
        recycle_to_pool (bool, optional): Add generated molecules back to pool for further
                                         combination. Creates iterative growth. Default: True
        max_heavy_atoms (int, optional): Maximum heavy atoms in combined molecules.
                                        Prevents runaway growth. Default: 20
        sterioisomers (bool, optional): Enumerate and randomly select stereoisomers of
                                       products. Default: False
        tautomers (bool, optional): Enumerate and randomly select tautomers of products.
                                   Default: False
        heterocycles (bool, optional): Enumerate and randomly select heterocyclic variants
                                      of products. Default: False
    
    Returns:
        set: Set of unique SMILES strings representing generated molecules.
             Size may be less than n_mol_needed if max_run is reached first.

    """

    out_pool = set()
    mol_pool = set(mol_pool)

    for i in range(max_run):
        # Randomly select two molecules from the pool
        mol1 = random.choice(list(mol_pool))
        mol2 = random.choice(list(mol_pool))

        # Add a filter on large items
        if Chem.MolFromSmiles(mol1).GetNumHeavyAtoms() + Chem.MolFromSmiles(mol2).GetNumHeavyAtoms() > max_heavy_atoms:
            print(f'Skipping combination due to heavy atom count exceeding {max_heavy_atoms}.', flush=True)
            continue

        # React the two molecules
        new_mol = react_smiles(mol1, mol2, random_bond=random_bond, con_filter=con_filter)

        if sterioisomers and new_mol is not None:
            # Enumerate stereoisomers if requested
            new_mol_sterio = enumerate_sterioisomers(Chem.MolFromSmiles(new_mol))
            if len(new_mol_sterio) != 0:
                # Pick a random stereoisomer from the list
                new_mol = Chem.MolToSmiles(random.choice(new_mol_sterio))

        if tautomers and new_mol is not None:
            # Enumerate tautomers if requested
            new_mol_taut = enumerate_tautomers(Chem.MolFromSmiles(new_mol))
            if len(new_mol_taut) != 0:
                # Pick a random tautomer from the list
                new_mol = Chem.MolToSmiles(random.choice(new_mol_taut))

        if heterocycles and new_mol is not None:
            # Enumerate heterocycles if requested
            new_mol_het = enumerate_heterocycles(Chem.MolFromSmiles(new_mol))
            if len(new_mol_het) != 0:
                # Pick a random heterocycle from the list
                new_mol = Chem.MolToSmiles(random.choice(new_mol_het))

        # Check if the new molecule is valid
        if new_mol is not None:
            out_pool.add(new_mol)
            if recycle_to_pool:
                # Add the new molecule back to the pool for further reactions
                mol_pool.add(new_mol)

        # Check if we have enough unique molecules
        if len(out_pool) >= n_mol_needed:
            print(f'Found {len(out_pool)} unique molecules.', flush=True)
            break

        # Check if we hit the maximum number of iterations
        if i == max_run - 1:
            print(f'Maximum iterations reached: {max_run}.', flush=True)

    return out_pool


